if ( ! function_exists( 'boldthemes_customize_custom_osm_map_style' ) ) {
	function boldthemes_customize_custom_osm_map_style( $wp_customize ) {
		$wp_customize->add_setting( BoldThemesFramework::$pfx . '_theme_options[custom_osm_map_style]', array(
			'type'              => 'option',
			'capability'        => 'edit_theme_options',
			'sanitize_callback' => 'boldthemes_custom_js'
		));
		$wp_customize->add_control( new BoldThemes_Customize_Textarea_Control( 
			$wp_customize, 
			'custom_osm_map_style', array(
				'label'    => esc_html__( 'Custom OSM Map Style', 'bello' ),
				'section'  => BoldThemesFramework::$pfx . '_listing_map_section',
                                'settings' => BoldThemesFramework::$pfx . '_theme_options[custom_osm_map_style]',
				'description' => esc_html__( 'Set a custom OSM map style by using a custom map URLs. ' , 'bello')
                                        . sprintf(
                                            '<a target="_blank" href="%s">%s</a>.',
                                            esc_url( 'https://wiki.openstreetmap.org/wiki/Tile_servers' ),
                                            esc_html__( 'Find out more', 'bello' )
                                        ),
                               'tooltip'    => esc_html__( 'Click to show/hide example' , 'bello'),
                               'example'    => esc_html__( 'Example:' , 'bello')
                                        . sprintf(
                                            '<br />"%s","%s","%s"',
                                            'http://a.tile.openstreetmap.fr/hot/${z}/${x}/${y}.png',
                                            'http://b.tile.openstreetmap.fr/hot/${z}/${x}/${y}.png',
                                            'http://c.tile.openstreetmap.fr/hot/${z}/${x}/${y}.png'
                                        ),
                               'priority'   => 416
				
			)
		));
	}
}
 
add_action( 'customize_register', 'boldthemes_customize_custom_osm_map_style' );
