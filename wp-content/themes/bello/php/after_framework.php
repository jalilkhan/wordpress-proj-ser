// LISTING DEFAULT IMAGE
if ( ! function_exists( 'boldthemes_customize_listing_default_image' ) ) {
	function boldthemes_customize_listing_default_image( $wp_customize ) {
		
		$wp_customize->add_setting( BoldThemesFramework::$pfx . '_theme_options[listing_default_image]', array(
			'default'           => BoldThemes_Customize_Default::$data['listing_default_image'],
			'type'              => 'option',
			'capability'        => 'edit_theme_options',
			'sanitize_callback' => 'boldthemes_sanitize_image'
		));
		$wp_customize->add_control( new WP_Customize_Image_Control( $wp_customize, 'listing_default_image', array(
			'label'    => esc_html__( 'Listing Default Image', 'bello' ),
			'section'  => BoldThemesFramework::$pfx . '_listing_general_section',
			'settings' => BoldThemesFramework::$pfx . '_theme_options[listing_default_image]',
			'description' => esc_html__( 'Set a default image used when a Listing doesn\'t have a featured image. Used only for Listing Search and shortcodes.', 'bello'  ),
			'priority' => 1,
			'context'  => BoldThemesFramework::$pfx . '_listing_default_image'
		)));
	}
}
add_action( 'customize_register', 'boldthemes_customize_listing_default_image' );
